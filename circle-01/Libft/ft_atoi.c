#include "libft.h"

int	ft_atoi(const char *s)
{
	int	sign;
	int	res;

	sign = 1;
	res = 0;
	while (*s == ' ' || *s == '\n' || *s == '\t' ||
			*s == '\v' || *s == '\r' || *s == '\f')
		++s;
	if (*s == '-' || *s == '+')
		if (*s++ == '-')
			sign = -1;
	while (ft_isdigit(*s))
		res = res * 10 + (*s++ - '0');
	return (res * sign);
}
