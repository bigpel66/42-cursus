#include "ft_printf.h"

t_bool	alloc_null_wstr(wchar_t **buf)
{
	if (!dalloc((void **)buf, 7, sizeof(wchar_t)))
		return (FL);
	(*buf)[0] = '(';
	(*buf)[1] = 'n';
	(*buf)[2] = 'u';
	(*buf)[3] = 'l';
	(*buf)[4] = 'l';
	(*buf)[5] = ')';
	return (TR);
}
