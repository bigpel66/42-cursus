#include "libft.h"

int	ft_atoi(const char *s)
{
	int			sign;
	long long	res;

	sign = 1;
	res = 0;
	while (*s == ' ' || *s == '\n' || *s == '\t' ||
			*s == '\v' || *s == '\r' || *s == '\f')
		++s;
	if (*s == '-' || *s == '+')
		if (*s++ == '-')
			sign = -1;
	while (ft_isdigit(*s))
	{
		if ((res >= 0) != ((res << 1) >= 0) ||
				(res >= 0) != ((res << 2) >= 0) ||
				(res >= 0) != ((res << 3) >= 0) ||
				(res >= 0) != (res * 10 + (*s - '0') >= 0))
			return (sign == 1 ? -1 : 0);
		res = res * 10 + (*s++ - '0');
	}
	return (res * sign);
}
