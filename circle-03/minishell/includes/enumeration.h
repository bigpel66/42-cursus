#ifndef ENUMERATION_H
# define ENUMERATION_H

/*
** =============================================================================
** Exit Code Definition
** =============================================================================
*/

typedef enum e_exit
{
	VALID = 0,
	GENERAL = 1,
	BUILTIN = 2,
	NOTEXECUTABLE = 126,
	NOTFOUND = 127,
	ARGUMENT = 128,
	INTERRUPT = 130,
	RANGE = 255,
}			t_exit;

/*
** =============================================================================
** RB Tree Node Color Definition
** =============================================================================
*/

typedef enum e_color
{
	RED,
	BLACK,
}					t_color;

/*
** =============================================================================
** AS Tree Syntax Type Definition
** =============================================================================
*/

typedef enum e_type
{
	SCLN = -1,
	PIPE,
	RDR,
	CMD,
}					t_type;

/*
** =============================================================================
** Process In or Out Definition
** =============================================================================
*/

typedef enum e_inout
{
	IN,
	OUT
}					t_inout;

#endif
