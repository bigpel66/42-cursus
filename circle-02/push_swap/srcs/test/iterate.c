void	iterate(t_ps **ps, int cnt, void (*f)(t_ps **))
{
	uint64_t delay;

	while (cnt--)
	{
		delay = 250000000;
		while (delay--)
			;
		f(ps);
		print_stacks(ps);
	}
}
